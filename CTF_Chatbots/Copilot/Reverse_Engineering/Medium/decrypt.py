xor_key = [0x13, 0x37, 0x42, 0x21, 0x7F]
target  = [0x75, 0x52, 0x26, 0x44, 0x1A]

# Recover original input
password = ''.join([chr(t ^ xor_key[i % 5]) for i, t in enumerate(target)])
print(f"Recovered password: {password}")

# Decrypt the flag
flag_enc = [0x53, 0x5B, 0x4C, 0x4A, 0x5A, 0x0F, 0x1F, 0x0C, 0x1F, 0x0C, 0x1F, 0x0C, 0x1F, 0x0C, 0x1F, 0x0C]
flag = ''.join([chr(b ^ xor_key[i % 5]) for i, b in enumerate(flag_enc)])
print(f"Decrypted flag: {flag}")
